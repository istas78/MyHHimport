package ru.ist_as.hhimport.vacancies.item;

public class Type {
    private String id;
    private String name;

    public Type() {
    }
// Getter Methods

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    // Setter Methods

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
