package ru.ist_as.hhimport.vacancies.item;

public class Area {
    private String id;
    private String name;
    private String url;

    public Area() {
    }
// Getter Methods

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    // Setter Methods

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
